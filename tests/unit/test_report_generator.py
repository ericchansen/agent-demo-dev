class TestGeneratePptx:
    """Tests for generate_pptx."""

    def test_output_file_exists(
        self, tmp_path: Path, sample_pipeline_data: list[dict], sample_research_data: dict
    ) -> None:
        data = _make_report_data(sample_pipeline_data, sample_research_data, SAMPLE_SP_DOCS)
        out = tmp_path / "deck.pptx"
        result = generate_pptx(data, "qbr_deck.md", str(out))
        assert Path(result).exists()

    def test_has_expected_slides(
        self, tmp_path: Path, sample_pipeline_data: list[dict], sample_research_data: dict
    ) -> None:
        data = _make_report_data(sample_pipeline_data, sample_research_data, SAMPLE_SP_DOCS)
        out = tmp_path / "deck.pptx"
        generate_pptx(data, "qbr_deck.md", str(out))

        prs = Presentation(str(out))
        assert len(prs.slides) == 4

    def test_title_slide_content(
        self, tmp_path: Path, sample_pipeline_data: list[dict], sample_research_data: dict
    ) -> None:
        data = _make_report_data(sample_pipeline_data, sample_research_data)
        out = tmp_path / "deck.pptx"
        generate_pptx(data, "qbr_deck.md", str(out))

        prs = Presentation(str(out))
        title_slide = prs.slides[0]
        assert title_slide.shapes.title.text == "Account Plan — Contoso Ltd"

    def test_pipeline_slide_has_table(
        self, tmp_path: Path, sample_pipeline_data: list[dict], sample_research_data: dict
    ) -> None:
        data = _make_report_data(sample_pipeline_data, sample_research_data)
        out = tmp_path / "deck.pptx"
        generate_pptx(data, "qbr_deck.md", str(out))

        prs = Presentation(str(out))
        pipeline_slide = prs.slides[1]
        tables = [s for s in pipeline_slide.shapes if s.has_table]
        assert len(tables) == 1
        assert len(tables[0].table.rows) == 4  # header + 3 deals

    def test_citations_slide(
        self, tmp_path: Path, sample_pipeline_data: list[dict], sample_research_data: dict
    ) -> None:
        data = _make_report_data(sample_pipeline_data, sample_research_data, SAMPLE_SP_DOCS)
        out = tmp_path / "deck.pptx"
        generate_pptx(data, "qbr_deck.md", str(out))

        prs = Presentation(str(out))
        citations_slide = prs.slides[3]
        all_text = " ".join(shape.text_frame.text for shape in citations_slide.shapes if shape.has_text_frame)
        assert "Fabric pipeline query" in all_text
        assert "SharePoint: FY25 Account Strategy" in all_text

    def test_footer_on_all_slides(
        self, tmp_path: Path, sample_pipeline_data: list[dict], sample_research_data: dict
    ) -> None:
        data = _make_report_data(sample_pipeline_data, sample_research_data)
        out = tmp_path / "deck.pptx"
        generate_pptx(data, "qbr_deck.md", str(out))

        prs = Presentation(str(out))
        for slide in prs.slides:
            all_text = " ".join(shape.text_frame.text for shape in slide.shapes if shape.has_text_frame)
            assert "Generated by Fabric Sales Agent Accelerator" in all_text

    def test_minimal_data(self, tmp_path: Path) -> None:
        """Empty pipeline, empty articles, no SP docs."""
        data = _make_report_data(
            pipeline_data=[],
            research_data={"company_name": "Minimal Corp", "articles": []},
        )
        out = tmp_path / "minimal.pptx"
        result = generate_pptx(data, "qbr_deck.md", str(out))
        assert Path(result).exists()

        prs = Presentation(str(out))
        assert len(prs.slides) == 4

    def test_intelligence_slide_bullets(
        self, tmp_path: Path, sample_pipeline_data: list[dict], sample_research_data: dict
    ) -> None:
        data = _make_report_data(sample_pipeline_data, sample_research_data)
        out = tmp_path / "deck.pptx"
        generate_pptx(data, "qbr_deck.md", str(out))

        prs = Presentation(str(out))
        intel_slide = prs.slides[2]
        all_text = " ".join(shape.text_frame.text for shape in intel_slide.shapes if shape.has_text_frame)
        assert "Contoso Reports Record Q2 Earnings" in all_text
        assert "Contoso Expands APAC Operations" in all_text
